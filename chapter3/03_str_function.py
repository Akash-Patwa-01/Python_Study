

name = "Akash is good boy"


print(len(name)) # length of string
print(name[0]) # first character of string
print(name[1]) # second character of string

print(name.endswith("h")) # check if string ends with "h"
print(name.count("a")) # count the number of "a" in string
print(name.find("h")) # find the index of "h" in string
print(name.index("h")) # find the index of "h" in string
print(name.isalpha()) # check if string is alphabetic
print(name.isalnum()) # check if string is alphanumeric
print(name.startswith('b')) # check if string starts with "b"
print(name.upper()) # convert string to uppercase
print(name.lower()) # convert string to lowercase
print(name.capitalize()) # capitalize first character of string

print(name.replace("good","bad")) # replace "good" with "bad" in string
print(name.split(" ")) # split string by space


